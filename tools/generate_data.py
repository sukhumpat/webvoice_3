# ---- ตัวเติมพยางค์ฝึกออกเสียง (drill) เมื่อคำจริงไม่ครบ 80 ----
VOWELS = [
    ("ะ","ะ"),("า","า"),("ิ","ิ"),("ี","ี"),("ึ","ึ"),("ื","ือ"),("ุ","ุ"),("ู","ู"),
    ("เ","เ-"),("แ","แ-"),("โ","โ-"),("ไ","ไ-"),("ใ","ใ-"),("ำ","ำ"),
]
FINALS = ["", "ง", "น", "ม", "ด", "บ", "ก"]

def make_drills(letter, need):
    """สร้างพยางค์ฝึกออกเสียง (พยัญชนะต้น + สระ + ตัวสะกด) ที่ออกเสียงได้จริง"""
    out = []
    leading = [("เ","เ"),("แ","แ"),("โ","โ"),("ไ","ไ"),("ใ","ใ")]
    for v_in, _ in VOWELS:
        for f in FINALS:
            if len(out) >= need:
                return out
            if v_in in ("เ","แ","โ","ไ","ใ"):      # สระหน้า
                if f and v_in in ("ไ","ใ"):
                    continue                        # ไ/ใ ไม่ประกอบตัวสะกด
                word = v_in + letter + f
            elif v_in == "ำ":
                if f:
                    continue
                word = letter + v_in
            else:                                   # สระบน/ล่าง/หลัง
                word = letter + v_in + f
            out.append((word, word, "🗣️"))
    return out

def build():
    rows = []          # สำหรับ CSV
    js_words = []       # สำหรับ JS fallback
    for letter, letter_name in CONSONANTS:
        pool = list(POOLS.get(letter, []))
        if len(pool) < 80:
            pool += make_drills(letter, 80 - len(pool))
        pool = pool[:80]
        for idx, age in enumerate([4, 5, 6, 7]):
            chunk = pool[idx*20:(idx+1)*20]
            code = "EX-%d-%s" % (age, letter)
            for order, (word, reading, emoji) in enumerate(chunk, start=1):
                row = {
                    "exercise_code": code,
                    "word": word,
                    "reading": reading,
                    "letter": letter,
                    "letter_name": letter_name,
                    "age_level": age,
                    "emoji": emoji,
                    "order_index": order,
                }
                rows.append(row)
                js_words.append(row)
    return rows, js_words

def main():
    here = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    rows, js_words = build()

    # data.csv
    csv_path = os.path.join(here, "db", "data.csv")
    os.makedirs(os.path.dirname(csv_path), exist_ok=True)
    fields = ["exercise_code","word","reading","letter","letter_name","age_level","emoji","order_index"]
    # ใช้ utf-8 (ไม่มี BOM) เพื่อให้ Supabase map คอลัมน์แรกถูกต้อง
    with open(csv_path, "w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fields)
        w.writeheader()
        w.writerows(rows)

    # words-data.js (fallback ฝั่ง client)
    js_path = os.path.join(here, "assets", "js", "words-data.js")
    os.makedirs(os.path.dirname(js_path), exist_ok=True)
    with open(js_path, "w", encoding="utf-8") as f:
        f.write("// ไฟล์นี้ถูกสร้างอัตโนมัติโดย tools/generate_data.py — ห้ามแก้ด้วยมือ\n")
        f.write("// ใช้เป็นคลังคำสำรอง (fallback) เมื่อยังไม่ได้ตั้งค่า Supabase\n")
        f.write("window.WORDS_DATA = ")
        json.dump(js_words, f, ensure_ascii=False, separators=(",", ":"))
        f.write(";\n")

    # สรุป
    print("Generated %d rows -> %s" % (len(rows), csv_path))
    print("Generated %s" % js_path)
    # ตรวจสอบความถูกต้อง: ทุก exercise ต้องมี 20 คำ, ทุกอายุมี 20 exercise
    from collections import Counter
    c = Counter(r["exercise_code"] for r in rows)
    assert all(v == 20 for v in c.values()), "บางแบบฝึกหัดมีคำไม่ครบ 20"
    assert len(c) == 80, "จำนวนแบบฝึกหัดต้องเท่ากับ 80 (20 ตัวอักษร x 4 อายุ)"
    print("OK: %d exercises x 20 words = %d (ครบถ้วน)" % (len(c), len(rows)))

if __name__ == "__main__":
    main()
